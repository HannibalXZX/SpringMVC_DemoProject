package com.xzx.model;

public class Patent {
    int Pid;
    String Pname;
    String Ppwd;

    public int getPid() {
        return Pid;
    }

    public void setPid(int pid) {
        Pid = pid;
    }

    public String getPname() {
        return Pname;
    }

    public void setPname(String pname) {
        Pname = pname;
    }

    public String getPpwd() {
        return Ppwd;
    }

    public void setPpwd(String ppwd) {
        Ppwd = ppwd;
    }
}
